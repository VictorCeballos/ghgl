
namespace ghgl
{
    struct Vec4
    {
        public float _x, _y, _z, _w;
        public Vec4(float a, float b, float c, float d)
        {
            _x = a;
            _y = b;
            _z = c;
            _w = d;
        }
        public override string ToString()
        {
            return $"{_x},{_y},{_z},{_w}";
        }
    }
}
